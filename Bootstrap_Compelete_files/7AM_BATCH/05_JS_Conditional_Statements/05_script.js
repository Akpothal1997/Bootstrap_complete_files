// If Else condition Example
let time = -5;
if(time >= 0 &&  time <= 12){
    console.log('Good Morning');
}
else if(time > 12 && time <= 17){
    console.log('Good Afternoon');
}
else if(time > 17 && time <= 23){
    console.log('Good Evening');
}
else{
    console.log('Please Proper Time Dude')
}

// Switch Statement Example
let month = 7;
let output = 'Month is : ';
switch(month){
    case 1:
        output += 'JANUARY';
        break;
    case 2:
        output += 'FEBRUARY';
        break;
    case 3:
        output += 'MARCH';
        break;
    case 4:
        output += 'APRIL';
        break;
    case 5:
        output += 'MAY';
        break;
    case 6:
        output += 'JUNE';
        break;
    case 7:
        output += 'JULY';
        break;
    case 8:
        output += 'AUGUST';
        break;
    case 9:
        output += 'SEPTEMBER';
        break;
    case 10:
        output += 'OCTOBER';
        break;
    case 11:
        output += 'NOVEMBER';
        break;
    case 12:
        output += 'DECEMBER';
        break;
    default:
        output += 'Enter Proper Month Number Dude';
        break;
}
console.log(output);


